#!/usr/bin/env python3
"""
First-Run Setup Wizard
======================
Two-bot architecture - no direct networking between machines.
Telegram acts as the message relay between Machine A and Machine B.

  Machine A - OpenClaw AI Bot  (@openclawbot)
    - Generates emails
    - Sends approval requests to Machine B's bot via Telegram
    - Receives decline callbacks via its own bot's incoming messages

  Machine B - Approval Bot  (@approvalbot)
    - Shows email preview with ✅ / ❌ buttons
    - ✅ sends via Gmail API
    - ❌ sends a signed decline message to Machine A's bot

Usage:
  python setup.py --machine a    # Configure Machine A (OpenClaw AI Bot)
  python setup.py --machine b    # Configure Machine B (Approval Bot + Gmail)
"""

import argparse
import getpass
import hmac
import hashlib
